export default function Sidebar() {
  const pathname = usePathname();
  const router = useRouter();
  const [user, setUser] = useState<{ name: string; email: string; initials: string } | null>(null);
  const { badges } = useNotifications();

  useEffect(() => {
    supabase.auth.getSession().then(({ data: { session } }) => {
      if (session?.user) {
        const meta = session.user.user_metadata;
        const name = meta?.full_name || meta?.name || session.user.email?.split("@")[0] || "User";
        const email = session.user.email || "";
        const initials = name
          .split(" ")
          .map((n: string) => n[0])
          .join("")
          .toUpperCase()
          .slice(0, 2);
        setUser({ name, email, initials });
      }
    });
  }, []);

  async function handleSignOut() {
    await supabase.auth.signOut();
    router.replace("/login");
  }

  return (
    <aside className="h-full bg-white border-r border-[#E0DED8] flex flex-col">
      {/* Logo */}
      <div className="h-16 flex items-center px-6 border-b border-[#E0DED8]">
        <div className="flex items-center gap-2.5">
          <img src="/logo.webp" alt="ARIA" className="h-8 w-8 rounded-full object-cover" />
          <span className="text-[#2C2C2A] font-semibold text-lg tracking-tight">ARIA</span>
        </div>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto">
        {navItems.map((item) => {
          const isActive = pathname === item.href || pathname?.startsWith(item.href + "/");
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? "bg-[#EEEDFE] text-[#534AB7]"
                  : (item as any).highlight
                  ? "text-[#534AB7] bg-[#534AB7]/5 hover:bg-[#EEEDFE] border border-[#534AB7]/20"
                  : "text-[#5F5E5A] hover:bg-[#F8F8F6] hover:text-[#2C2C2A]"
              }`}
            >
              {item.icon}
              <span>{item.label}</span>
              {(() => {
                const key = (item as any).badgeKey as keyof typeof badges | undefined;
                const count = key ? badges[key] || 0 : 0;
                if (!count) return null;
                return (
                  <span className="ml-auto bg-[#D85A30] text-white text-xs font-semibold px-2 py-0.5 rounded-full min-w-[20px] text-center">
                    {count > 99 ? "99+" : count}
                  </span>
                );
              })()}
            </Link>
          );
        })}
      </nav>

      {/* Bottom section — user profile + sign out */}
      <div className="p-4 border-t border-[#E0DED8]">
        <div className="flex items-center gap-3 px-2">
          <div className="w-8 h-8 rounded-full bg-[#EEEDFE] flex items-center justify-center">
            <span className="text-[#534AB7] text-xs font-semibold">{user?.initials || "--"}</span>
          </div>
          <div className="flex-1 min-w-0">
            <p className="text-sm font-medium text-[#2C2C2A] truncate">{user?.name || "Loading..."}</p>
            <p className="text-xs text-[#5F5E5A] truncate">{user?.email || ""}</p>
          </div>
          <button
            onClick={handleSignOut}
            title="Sign out"
            className="text-[#5F5E5A] hover:text-[#D85A30] transition-colors"
          >
            <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M15.75 9V5.25A2.25 2.25 0 0013.5 3h-6a2.25 2.25 0 00-2.25 2.25v13.5A2.25 2.25 0 007.5 21h6a2.25 2.25 0 002.25-2.25V15m3 0l3-3m0 0l-3-3m3 3H9" />
            </svg>
          </button>
        </div>
      </div>
    </aside>
  );
}
